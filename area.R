# Areaofcirlce, triangle, square

print('Area of circle: ')
radius <- as.integer(readline('Enter radius: '))
area <- pi * radius * radius
print(area)

print('Area of triangle: ')
base <- as.integer(readline('Enter base length: '))
height <- as.integer(readline('Enter base length: '))
area <- 0.5 * base * height
print(area)


print('Area of square: ')
side <- as.integer(readline('Enter side length: '))
area <- side * side
print(area)
